#include "ft_cub.h"

int	ft_isspace(char c)
{
	if (!c)
		return (0);
	if (c == '\t' || c == '\n' || c == '\v'
		|| c == '\f' || c == '\r' || c == ' ')
		return (1);
	return (0);
}

int	ft_is_valid_map(char c)
{
	if (c == '0' || c == '1' || c == '2' || c == 'N' || c == 'S' || c == 'W'
		|| c == 'E')
		return (1);
	return (0);
}
